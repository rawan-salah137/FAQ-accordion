function ans1() {
    const plus = document.querySelector(".plus1");
    const ans = document.querySelector(".ans1");

    if (plus.classList.contains("fa-circle-plus")) {
        plus.classList.remove("fa-circle-plus");
        plus.classList.add("fa-circle-minus");
        ans.style.display = "block";
    } else if (plus.classList.contains("fa-circle-minus")) {
        plus.classList.remove("fa-circle-minus");
        plus.classList.add("fa-circle-plus");
        ans.style.display = "none";
    }
}

